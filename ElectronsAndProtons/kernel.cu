	int blockY = 32;
	dim3 intensityBlockDim = dim3(blockX, blockY);
	dim3 intensityKernelGridDim = dim3((width + blockX - 1) / blockX, (height + blockY - 1) / blockY);
	intensityKernel<<<intensityKernelGridDim, intensityBlockDim>>>(
		grid, 
		field->field_d, 
		width, 
		height, 
		field->positions_d, 
		field->charges_d, 
		particlesCount,
		aspectRatio,
		grid_row_width,
		grid_column_height);

