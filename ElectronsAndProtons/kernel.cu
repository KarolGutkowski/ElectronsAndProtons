		field->particles_grid_cells_d, 
		grid_cells_indices,
		particlesCount,
		field->grid_columns * field->grid_rows, 
		field->grid_columns);
		

	err = cudaGetLastError();
	if (err != cudaSuccess)
	{
		std::cout << cudaGetErrorString(err) << std::endl;
		//assert(false);
	}

	float aspectRatio = width / height;
